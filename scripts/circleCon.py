# replace shape to simple circle

import maya.cmds as cmds

def run():
    cmds.promptDialog(m='CON axis:', tx='x')
    conAxis = cmds.promptDialog(q=1, tx=1)
    
    cmds.promptDialog(m='CON scale:', tx='10')
    scaleValue = float(cmds.promptDialog(q=1, tx=1))
    
    sels = cmds.ls(sl=1)
    
    for sel in sels:
        cmds.delete(cmds.listRelatives(sel, s=1))
        con = cmds.curve(d=1, p=([4.4096508999565238e-16, -6.2800017075224745e-17, 1.0259919109629996], [0.39242144509083166, -5.8008662454595322e-17, 0.94774315182746349], [0.72504382642569543, -4.4394718959682415e-17, 0.72541058207116293], [0.94736947046524878, -2.4025600781345162e-17, 0.39275763768721461], [1.0255956239906017, 2.4467037230532147e-21, 0.00034984301862106548], [0.94737790869272631, 2.4031469296989711e-17, -0.39207387637535618], [0.72492881611356719, 4.4398499557601711e-17, -0.72469272247518202], [0.39233737924706358, 5.8001512080169747e-17, -0.94684677590575639], [1.1494295749255114e-16, 6.2847753895966642e-17, -1.0259919109629978], [-0.39233737924706336, 5.8001512080169747e-17, -0.94684677590575639], [-0.72492881611356796, 4.4398499557601735e-17, -0.7246927224751829], [-0.94737790869272442, 2.403146929698977e-17, -0.39207387637535795], [-1.0255956239906023, 2.4467037230660651e-21, 0.00034984301862106548], [-0.94736947046524667, -2.4025600781345119e-17, 0.39275763768721283], [-0.72504382642569709, -4.4394718959682371e-17, 0.72541058207116382], [-0.39242144509083193, -5.8008662454595396e-17, 0.9477431518274626], [4.4096508999565238e-16, -6.2800017075224745e-17, 1.0259919109629996]))
        if conAxis == 'x':
            cmds.rotate(0, 0, 90, con, os=1, r=1)
        if conAxis == 'y':
            cmds.rotate(0, 90, 0, con, os=1, r=1)
        if conAxis == 'z':
            cmds.rotate(90, 0, 0, con, os=1, r=1)
        cmds.scale(scaleValue, scaleValue, scaleValue, con)
        cmds.makeIdentity(con, a=1)
        cmds.parent(cmds.listRelatives(con, s=1), sel, r=1, s=1)
        cmds.delete(con)
